	public void mostrar() {

		// buffer para las imagenes
		BufferStrategy buffer = getBufferStrategy();
		if (buffer == null) {
			createBufferStrategy(3);
			return;
		}

		// contexto para dibujar
		Graphics g = buffer.getDrawGraphics();
		// pintamos la imagen
		g.drawImage(imagen, 0, 0, getWidth(), getHeight(), this);
		// limpiamos el jframe
		g.dispose();
		// mostramos el hilo
		buffer.show();

	}

